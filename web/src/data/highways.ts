export function interpolatePosition(
  highway: HighwayDef,
  targetKm: number
): { lat: number; lon: number } | null {
  const wps = highway.waypoints
  if (wps.length === 0) return null

  if (targetKm <= wps[0].km) return { lat: wps[0].lat, lon: wps[0].lon }
  if (targetKm >= wps[wps.length - 1].km) return { lat: wps[wps.length - 1].lat, lon: wps[wps.length - 1].lon }

  for (let i = 0; i < wps.length - 1; i++) {
    const a = wps[i]
    const b = wps[i + 1]
    if (targetKm >= a.km && targetKm <= b.km) {
      const t = (targetKm - a.km) / (b.km - a.km)
      return {
        lat: a.lat + (b.lat - a.lat) * t,
        lon: a.lon + (b.lon - a.lon) * t,
      }
    }
  }
  return null
}
